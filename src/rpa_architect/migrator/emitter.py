def _transaction_view(tx: Transaction) -> dict[str, Any]:
    module = _module_name(tx.name)
    return {
        "name": tx.name,
        "module": module,
        "fn_name": module,
        "steps": [_step_view(s) for s in tx.steps],
        "selector_fingerprints": [
            fp for step in tx.steps for fp in _selector_fingerprints(step.actions)
        ],
    }


def _step_view(step: Step) -> dict[str, Any]:
    calls: list[str] = []
    for action in step.actions:
        try:
            calls.append(emit_call(action))
        except UnsupportedActionError as exc:
            calls.append(f"# TODO: unsupported UiPath action: {exc}")
    return {
        "id": step.id,
        "description": step.description,
        "type": step.type,
        "calls": calls,
    }


def _selector_fingerprints(actions: list[UIAction]) -> list[dict[str, Any]]:
    out: list[dict[str, Any]] = []
    for a in actions:
        if not a.selector_hint:
            continue
        expr = translate_selector(a.selector_hint)
        css = _extract_css(expr)
        if css is None:
            continue
        out.append({"target": a.target, "css_literal": repr(css)})
    return out


_LOCATOR_CSS_RE = re.compile(r"page\.locator\('([^']+)'\)")


def _extract_css(locator_expr: str) -> str | None:
    m = _LOCATOR_CSS_RE.search(locator_expr)
    return m.group(1) if m else None


_SNAKE_RE = re.compile(r"([A-Z])")


def _module_name(transaction_name: str) -> str:
    snake = _SNAKE_RE.sub(r"_\1", transaction_name).lower().lstrip("_")
    if not snake.startswith("process_"):
        snake = "process_" + snake
    return snake
