"""Turn a :class:`ProcessIR` into a runnable Python + Playwright project.

Implementation note — Jinja proved fragile for generating indented Python
inside nested ``for`` blocks (trim/lstrip combinations that keep the
``main.py`` skeleton valid also eat the function-body indentation we need
for ``process_<tx>.py``). We build the three Python files as plain strings
here, keep Jinja for ``pyproject.toml`` / ``README.md`` where the output
format is looser.
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Any

from jinja2 import Environment, FileSystemLoader

from rpa_architect.ir.schema import ProcessIR, Step, Transaction, UIAction
from rpa_architect.migrator.activity_map import (
    UnsupportedActionError,
    emit_call,
)
from rpa_architect.migrator.selector_translator import translate_selector

TEMPLATES_DIR = Path(__file__).resolve().parents[3] / "templates" / "migrator"


def emit_project(ir: ProcessIR, output_dir: Path | str) -> None:
    """Render the full project into ``output_dir``."""
    out = Path(output_dir)
    (out / "processes").mkdir(parents=True, exist_ok=True)
    (out / "tests").mkdir(parents=True, exist_ok=True)

    views = [_transaction_view(tx) for tx in ir.transactions]

    (out / "main.py").write_text(_render_main(ir.process_name, views))
    for tx in views:
        (out / "processes" / f"{tx['module']}.py").write_text(_render_process(tx))
        (out / "tests" / f"test_parity_{tx['module']}.py").write_text(_render_parity(tx))

    env = Environment(
        loader=FileSystemLoader(str(TEMPLATES_DIR)),
        autoescape=False,
        trim_blocks=True,
        lstrip_blocks=True,
    )
    ctx = {"process_name": ir.process_name, "transactions": views}
    (out / "pyproject.toml").write_text(env.get_template("pyproject.toml.j2").render(**ctx))
    (out / "README.md").write_text(env.get_template("README.md.j2").render(**ctx))

    (out / "__init__.py").write_text("")
    (out / "tests" / "__init__.py").write_text("")
    (out / "processes" / "__init__.py").write_text("")


